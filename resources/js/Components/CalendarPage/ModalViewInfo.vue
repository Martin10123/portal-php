<script setup>
import Modal from '../Modal.vue';
import { onMounted, watch, computed, ref } from 'vue';
import ItemModalInfo from './ItemModalInfo.vue';
import EditIcon from '@/Assets/EditIcon.vue';
import { usePage } from '@inertiajs/vue3';
import { format } from 'date-fns';
import { es } from 'date-fns/locale';

const page = usePage()

const props = defineProps({
    infoSelectedEvent: Object,
    onViewInfoEvent: Function,
    onEditEvent: Function,
    onDeleteEvent: Function,
})

const isUserCreator = computed(() => {
    return props.infoSelectedEvent.uidUser === page.props.auth.user.guid
        || page.props.auth.user.IdResponsable === '20258'
        || page.props.auth.user.IdResponsable === '61'
        || page.props.auth.user.IdResponsable === '20187';
})

const disabledButton = ref(false);
const openModalInfo = ref(false);
const reasonUpdate = ref(null);

const onCloseModal = () => {
    openModalInfo.value = false;
    props.infoSelectedEvent.value = null;
}

const formatDate = (date) => {
    const dateFormat = format(new Date(date), "EEEE, d 'de' MMMM 'de' yyyy", { locale: es });
    const timeFormat = format(new Date(date), "HH:mm:ss", { locale: es });

    return `${dateFormat} a las ${timeFormat}`;
};

const getReasonUpdateOrCancel = async () => {
    try {

        const response = await axios.get(route("calendarPage.getReason", { id: props.infoSelectedEvent.id }));

        if (response.data.ok) {
            reasonUpdate.value = response.data.data.Log_Test;
        }

    } catch (error) {
        console.log(error);

    }
}

watch(() => openModalInfo.value, (value) => {
    if (!value) {
        props.onViewInfoEvent();
    }
})

const onEditThisEvent = () => {
    props.onEditEvent();
}

onMounted(() => {
    openModalInfo.value = true

    const validateIsPossibleEditDelete = () => {
        const currentDate = new Date();
        const eventDate = new Date(props.infoSelectedEvent.start);
        const currentHour = currentDate.getHours();
        const eventHour = eventDate.getHours();
        const currentMinutes = currentDate.getMinutes();
        const eventMinutes = eventDate.getMinutes();

        if (currentDate.getDate() > eventDate.getDate() ||
            (currentDate.getDate() === eventDate.getDate() && currentHour > eventHour) ||
            (currentDate.getDate() === eventDate.getDate() && currentHour === eventHour && currentMinutes > eventMinutes)
        ) {
            disabledButton.value = true;
        }
    }

    validateIsPossibleEditDelete()
    getReasonUpdateOrCancel()

})

</script>
